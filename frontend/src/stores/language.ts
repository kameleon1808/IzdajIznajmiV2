type MessageKey = keyof typeof enMessages

const messages: Record<LanguageCode, Record<MessageKey, string>> = {
  en: enMessages,
  sr: srMessages,
  de: enMessages,
  es: enMessages,
}

const isSupportedLanguage = (value: string): value is LanguageCode =>
  languageOptions.some((lang) => lang.code === value)

const loadLanguage = (): LanguageCode => {
  if (typeof localStorage === 'undefined') return DEFAULT_LANGUAGE
  const stored = localStorage.getItem(STORAGE_KEY)
  if (!stored) return DEFAULT_LANGUAGE
  return isSupportedLanguage(stored) ? stored : DEFAULT_LANGUAGE
}

export const useLanguageStore = defineStore('language', {
  state: () => ({
    current: loadLanguage() as LanguageCode,
  }),
  getters: {
    languages: () => languageOptions,
    t: (state) => (key: MessageKey) => {
      const table = messages[state.current] ?? messages.en
      return table[key] ?? messages.en[key]
    },
  },
  actions: {
    setLanguage(code: LanguageCode) {
      const next = isSupportedLanguage(code) ? code : DEFAULT_LANGUAGE
      this.current = next
      this.persist()
    },
    persist() {
      if (typeof localStorage === 'undefined') return
      localStorage.setItem(STORAGE_KEY, this.current)
    },
  },
})
